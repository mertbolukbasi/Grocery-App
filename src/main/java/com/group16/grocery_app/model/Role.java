package com.group16.grocery_app.model;

/**
 * Represents user roles in the grocery application.
 *
 * @author Mert Bölükbaşı
 */
public enum Role {
    /**
     * Customer role for regular shoppers.
     */
    CUSTOMER,

    /**
     * Carrier role for delivery personnel.
     */
    CARRIER,

    /**
     * Owner role for store administrators.
     */
    OWNER
}